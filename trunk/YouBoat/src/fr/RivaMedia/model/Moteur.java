package fr.RivaMedia.model;

public class Moteur {
	private String infoMoteur;
	private String propulsion;
	private String heureMoteur;
	private String marqueMoteur;
	private String puissanceMoteur;
	private Integer puissNumber;

	public String getInfoMoteur() {
		return infoMoteur;
	}
	public void setInfoMoteur(String infoMoteur) {
		this.infoMoteur = infoMoteur;
	}
	public String getPropulsion() {
		return propulsion;
	}
	public void setPropulsion(String propulsion) {
		this.propulsion = propulsion;
	}
	public String getHeureMoteur() {
		return heureMoteur;
	}
	public void setHeureMoteur(String heureMoteur) {
		this.heureMoteur = heureMoteur;
	}
	public String getMarqueMoteur() {
		return marqueMoteur;
	}
	public void setMarqueMoteur(String marqueMoteur) {
		this.marqueMoteur = marqueMoteur;
	}
	public String getPuissanceMoteur() {
		return puissanceMoteur;
	}
	public void setPuissanceMoteur(String puissanceMoteur) {
		this.puissanceMoteur = puissanceMoteur;
	}
	public Integer getPuissNumber() {
		return puissNumber;
	}
	public void setPuissNumber(Integer puissNumber) {
		this.puissNumber = puissNumber;
	}

}
