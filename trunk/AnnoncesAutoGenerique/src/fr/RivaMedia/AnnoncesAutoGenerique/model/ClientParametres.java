package fr.RivaMedia.AnnoncesAutoGenerique.model;

public class ClientParametres {

	String id;
	String texteIntro;
	String couleurPrincipale;
	String couleurSecondaire;
	String imageFond;
	String imageAccueil;
	String imageLogo;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTexteIntro() {
		return texteIntro;
	}
	public void setTexteIntro(String texteIntro) {
		this.texteIntro = texteIntro;
	}
	public String getCouleurPrincipale() {
		return couleurPrincipale;
	}
	public void setCouleurPrincipale(String couleurPrincipale) {
		this.couleurPrincipale = couleurPrincipale;
	}
	public String getCouleurSecondaire() {
		return couleurSecondaire;
	}
	public void setCouleurSecondaire(String couleurSecondaire) {
		this.couleurSecondaire = couleurSecondaire;
	}
	public String getImageFond() {
		return imageFond;
	}
	public void setImageFond(String imageFond) {
		this.imageFond = imageFond;
	}
	public String getImageAccueil() {
		return imageAccueil;
	}
	public void setImageAccueil(String imageAccueil) {
		this.imageAccueil = imageAccueil;
	}
	public String getImageLogo() {
		return imageLogo;
	}
	public void setImageLogo(String imageLogo) {
		this.imageLogo = imageLogo;
	}
	
}
